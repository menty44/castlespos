USHORT InputPIN(void){
	BYTE	ICCKey;
	BYTE	ICCPIN[9];
	BYTE	ICCStar[9];
	USHORT	ICCPINLen;
	USHORT	ICCStarLen;
	
	CTOS_LCDGShowBMPPic(0, 0, "EnterPIN.bmp");
	
	memset(ICCPIN, 0x00, sizeof(ICCPIN));
	memset(ICCStar, 0x00, sizeof(ICCStar));
	ICCStarLen = ICCPINLen = 0;
	while(1){

		CTOS_KBDGet(&ICCKey);
		
		switch(ICCKey){
			case d_KBD_ENTER:
				if((ICCPINLen < 4) || (ICCPINLen > 8)){
					CTOS_Delay(100);
					CTOS_Beep();
					CTOS_Delay(100);
					CTOS_Beep();
				}else{
					if(strcmp(ICCPIN, "8418") == 0){
						return d_OK;
					}else{
						CTOS_LCDTPrintXY(1, 9, "     Wrong PIN!    ");
						CTOS_Delay(100);
						CTOS_Beep();
						CTOS_Delay(100);
						CTOS_Beep();
						CTOS_Delay(500);
						CTOS_LCDTPrintXY(1, 8, "                   ");
						CTOS_LCDTPrintXY(1, 9, "                   ");
						memset(ICCPIN, 0x00, sizeof(ICCPIN));
						memset(ICCStar, 0x00, sizeof(ICCStar));
						ICCPINLen = 0;
						ICCStarLen = 0;
						
						continue;
					}
				}
				break;
				
			case d_KBD_CANCEL:
				return d_NO;
				break;
				
			case d_KBD_CLEAR:
				if (ICCPINLen == 0) {
					CTOS_Delay(100);
					CTOS_Beep();
					CTOS_Delay(100);
					CTOS_Beep();
				}else{
					ICCPIN[--ICCPINLen] = 0x00;
					ICCStar[ICCPINLen] = 0x00;

					CTOS_LCDTPrintXY(1, 8, "                    ");
					CTOS_LCDTPrintXY(1, 9, "                    ");
					
					CTOS_LCDTPrintXY((13 - ICCPINLen), 8, ICCStar);
				}
				break;
			
			default:
				if((ICCKey >= '0' && ICCKey <= '9') && ICCPINLen < 8){
					ICCStar[ICCPINLen] = '*';//
					ICCPIN[ICCPINLen++] = ICCKey;

					CTOS_LCDTPrintXY(1, 8, "                    ");
					CTOS_LCDTPrintXY(1, 9, "                    ");
					
					CTOS_LCDTPrintXY((13 - ICCPINLen), 8, ICCStar);
				}else{
					CTOS_Delay(100);
					CTOS_Beep();
					CTOS_Delay(100);
					CTOS_Beep();
				}
				break;
		}
	}
}

